// FunÃ§Ã£o para buscar receitas por termo
export const searchRecipes = (query: string): Recipe[] => {
  const lowerQuery = query.toLowerCase().trim();
  
  if (!lowerQuery) return [];
  
  return EXTERNAL_RECIPES.filter(recipe => {
    // Busca no tÃ­tulo
    if (recipe.title.toLowerCase().includes(lowerQuery)) return true;
    
    // Busca na descriÃ§Ã£o
    if (recipe.description.toLowerCase().includes(lowerQuery)) return true;
    
    // Busca nas tags
    if (recipe.tags.some(tag => tag.toLowerCase().includes(lowerQuery))) return true;
    
    // Busca nos ingredientes
    if (recipe.ingredients.some(ing => ing.name.toLowerCase().includes(lowerQuery))) return true;
    
    // Busca na categoria
    if (recipe.category.toLowerCase().includes(lowerQuery)) return true;
    
    return false;
  });
};

// FunÃ§Ã£o para obter sugestÃµes de busca
export const getSearchSuggestions = (query: string): string[] => {
  const lowerQuery = query.toLowerCase().trim();
  
  if (!lowerQuery || lowerQuery.length < 2) return [];
  
  const suggestions = new Set<string>();
  
  EXTERNAL_RECIPES.forEach(recipe => {
    // Adiciona tÃ­tulos que correspondem
    if (recipe.title.toLowerCase().includes(lowerQuery)) {
      suggestions.add(recipe.title);
    }
    
    // Adiciona ingredientes que correspondem
    recipe.ingredients.forEach(ing => {
      if (ing.name.toLowerCase().includes(lowerQuery)) {
        suggestions.add(ing.name);
      }
    });
    
    // Adiciona tags que correspondem
    recipe.tags.forEach(tag => {
      if (tag.toLowerCase().includes(lowerQuery)) {
        suggestions.add(tag);
      }
    });
  });
  
  return Array.from(suggestions).slice(0, 8);
};

